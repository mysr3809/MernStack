import mongoose from "mongoose";

const review = mongoose.Schema({
    country:{
        type: String,
        required: true
    },
    city:{
        type: String,
        required: true
    },
    address:{
        type: String,
        required: true
    },
    title:{
        type: String,
        required: true
    },
    description:{
        type: String,
        required: true
    },
});

const Review = mongoose.model('Review', review);
export default Review;